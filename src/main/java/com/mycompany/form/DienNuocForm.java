    private void listNhaTroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_listNhaTroActionPerformed
        // TODO add your handling code here:
        String a = (String) listNhaTro.getSelectedItem();
        String sql = "SELECT NhaTro.idNhaTro FROM NhaTro WHERE tenNhaTro=?";

        String query = "SELECT nt.idNhaTro, nt.tenNhaTro, lp.idLoaiPhong, lp.tenLoaiPhong, p.idPhong, p.tenPhong " +
        "FROM NhaTro AS nt " +
        "JOIN LoaiPhong AS lp ON nt.idNhaTro = lp.idNhaTro " +
        "JOIN Phong AS p ON lp.idLoaiPhong = p.idLoaiPhong " +
        "WHERE nt.idNhaTro = ?";

        try (Connection con = ketNoiDB.ketNoiDB.layKetNoi();
            PreparedStatement pss = con.prepareStatement(sql)) {

            pss.setString(1, a);

            String b = null;
            try (ResultSet rss = pss.executeQuery()) {
                if (rss.next()) {
                    b = rss.getString("idNhaTro");
                }
            }

            if (b != null) {
                try (PreparedStatement ps = con.prepareStatement(query)) {
                    ps.setString(1, b);

                    try (ResultSet rs = ps.executeQuery()) {
                        listphongtro.removeAllItems(); // Xóa tất cả các mục hiện tại trong listphongtro

                        while (rs.next()) {
                            String tenpt = rs.getString("tenPhong");
                            listphongtro.addItem(tenpt); // Thêm các mục mới vào listphongtro
                        }
                    }
                }
            }
        } catch (SQLException exSQL) {
        }

    }//GEN-LAST:event_listNhaTroActionPerformed

