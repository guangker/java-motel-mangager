    public void layttd(String a){
        Connection con = ketNoiDB.ketNoiDB.layKetNoi();
        String selectSql = "SELECT p.tenPhong, d.oldValue, d.Value " +
        "FROM phong AS p " +
        "JOIN ElectricMeter AS d ON p.idDien = d.idDien " +
        "WHERE p.tenPhong = ?";
        

        try {
            // Truy vấn giá trị hiện tại
            PreparedStatement selectPs = con.prepareStatement(selectSql);
            selectPs.setString(1, a);
            ResultSet rs = selectPs.executeQuery();

            String dc = null;

            if (rs.next()) {
                dc = rs.getString("oldValue");
                diencuu.setText(dc);
                String dmm = rs.getString("Value");
                dm.setText(dmm);
                SharedData.getInstance().setTd(dmm);
                float x=Float.parseFloat(diencuu.getText());
                float y=Float.parseFloat(dm.getText());
                float c=y-x;
                dtt.setText(String.valueOf(c));
            }
            rs.close();
            selectPs.close();
            con.close();
        } catch (SQLException exSQL) {
        }
    }
